import java.util.*;
public class HashSetDemo {

    public static void main(String args[]) {
        // Создаём HashSet
        HashSet hs = new HashSet();

        // Добавляем элементы к HashSet
        hs.add("B");
        hs.add("A");
        hs.add("D");
        hs.add("E");
        hs.add("C");
        hs.add("F");
        System.out.println(hs);
    }
}
